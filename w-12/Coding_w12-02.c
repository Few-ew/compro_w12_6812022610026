#include <stdio.h>

// ฟังก์ชัน swap รับ pointer ของ int สองตัว แล้วสลับค่ากัน
void swap(int *a, int *b) {
    int temp = *a;   // เก็บค่าของ *a ไว้ใน temp
    *a = *b;         // นำค่าของ *b ไปใส่ที่ *a
    *b = temp;       // นำค่าของ temp (ค่าของ *a เดิม) ไปใส่ที่ *b
}

int main() {
    int a = 5, b = 10; // กำหนดค่าเริ่มต้นให้ a เป็น 5 และ b เป็น 10

    printf("Before swap: a = %d b = %d\n", a, b); // แสดงค่าก่อนสลับ

    swap(&a, &b); // เรียกใช้ฟังก์ชัน swap โดยส่ง address ของ a และ b

    printf("After swap: a = %d b = %d\n", a, b); // แสดงค่าหลังสลับ

    return 0;
}
/*
ขั้นตอนการทำงานของโปรแกรม:
1. กำหนดค่า a เป็น 5 และ b เป็น 10
2. แสดงค่าของ a และ b ก่อนสลับ
3. เรียกใช้ฟังก์ชัน swap โดยส่ง address ของ a และ b เพื่อสลับค่ากัน
4. แสดงค่าของ a และ b หลังสลับ (a จะเป็น 10, b จะเป็น 5)
*/