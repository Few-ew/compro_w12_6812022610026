#include <stdio.h>
int main() {
    int x = 10;           // กำหนดค่าเริ่มต้นให้ตัวแปร x เป็น 10
    int *ptr;             // ประกาศ pointer ชนิด int ชื่อ ptr

    ptr = &x;             // ให้ ptr ชี้ไปที่ตำแหน่งของตัวแปร x

    printf("Before: x %d\n", x); // แสดงค่าของ x ก่อนเปลี่ยน

    *ptr = 20;            // ใช้ pointer เปลี่ยนค่าของ x ให้เป็น 20

    printf("After: x %d\n", x);  // แสดงค่าของ x หลังเปลี่ยน

    return 0;
}
/*
ขั้นตอนการทำงานของโปรแกรม:
1. กำหนดค่า x เป็น 10
2. ประกาศ pointer ptr สำหรับชี้ไปที่ตัวแปรชนิด int
3. ให้ ptr ชี้ไปที่ตำแหน่งของ x ด้วย &x
4. แสดงค่าของ x ก่อนเปลี่ยน (จะได้ค่า 10)
5. ใช้ pointer (*ptr) เปลี่ยนค่าของ x ให้เป็น 20
6. แสดงค่าของ x หลังเปลี่ยน (จะได้ค่า 20)
*/